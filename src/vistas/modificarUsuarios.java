    void listar(){
        List<Usuario> lista = ud.listar();
        modelo = (DefaultTableModel) tablaUsuarios.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < lista.size(); i++) {
            
            boolean comp = login.idSuc == lista.get(i).getId_sucursal();
            ob[0] = lista.get(i).getId();
            ob[1] = lista.get(i).getNombre();
            ob[2] = lista.get(i).getApellidoP();
            ob[3] = lista.get(i).getId_estado();
            ob[4] = lista.get(i).getUsername();
            ob[5] = lista.get(i).getId_tipo();
            
            if(comp == true){
                modelo.addRow(ob);
            }
        }
        tablaUsuarios.setModel(modelo);
    }
    
    void eliminar(){
        int fila = tablaUsuarios.getSelectedRow();
        
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila");
        }
        else
        {
            ud.eliminar(id);
        }
    }
    
    void actualizar(){
        int fila = tablaUsuarios.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "seleccione una fila");
        } else {
            String nom = txtNombre.getText();
            String ape = txtApe.getText();
            String estado = (cmb_estado.getSelectedItem().toString());
            String username = txtUsername.getText();
            String rol = cmb_rol.getSelectedItem().toString();
            
            Object[] ob = new Object[7];
            ob[0] = estado;
            ob[1] = username;
            ob[2] = nom;
            ob[3] = ape;
            ob[4] = rol;
            ob[5] = login.idSuc;
            ob[6] = id;
            ud.actualizar(ob);
        }
    }
    
    void limpiar(){
        txtApe.setText("");
        txtNombre.setText("");
        txtUsername.setText("");
    }
    
    void limpiarTabla(){
        modelo = (DefaultTableModel) tablaUsuarios.getModel();
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }
