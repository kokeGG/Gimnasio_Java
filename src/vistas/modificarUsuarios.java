    private void tablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaUsuariosMouseClicked
        // TODO add your handling code here:
        int fila = tablaUsuarios.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una fila");
        } else {
            id = Integer.parseInt(tablaUsuarios.getValueAt(fila, 0).toString());
            String estado = tablaUsuarios.getValueAt(fila, 3).toString();
            String usuario = tablaUsuarios.getValueAt(fila, 4).toString();
            String nom = tablaUsuarios.getValueAt(fila, 1).toString();
            String ape = tablaUsuarios.getValueAt(fila,2).toString();
            String rol = tablaUsuarios.getValueAt(fila, 5).toString();
            
            txtNombre.setText(nom);
            txtApe.setText(ape);
            cmb_estado.setSelectedItem(estado);
            txtUsername.setText(usuario);
            cmb_rol.setSelectedItem(rol);
        }
    }//GEN-LAST:event_tablaUsuariosMouseClicked

